package com.sist.cbox.vo;

public class SeatAccount {
	private String dealNum;
	private String mid;
	private String nmid;
	private String seatSeq;
	private String timeSeq;
	private String status;
	private String regdate;
	private String cancelCheck;
	
	
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	public String getCancelCheck() {
		return cancelCheck;
	}
	public void setCancelCheck(String cancelCheck) {
		this.cancelCheck = cancelCheck;
	}
	
	public String getDealNum() {
		return dealNum;
	}
	public void setDealNum(String dealNum) {
		this.dealNum = dealNum;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getNmid() {
		return nmid;
	}
	public void setNmid(String nmid) {
		this.nmid = nmid;
	}
	public String getSeatSeq() {
		return seatSeq;
	}
	public void setSeatSeq(String seatSeq) {
		this.seatSeq = seatSeq;
	}
	public String getTimeSeq() {
		return timeSeq;
	}
	public void setTimeSeq(String timeSeq) {
		this.timeSeq = timeSeq;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
